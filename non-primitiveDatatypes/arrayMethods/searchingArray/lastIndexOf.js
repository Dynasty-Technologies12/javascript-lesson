// lastIndexOf - returns the last index of the specified value, or -1 if the specific value is not found
const animals  = ["rat", "rodents", "cats", "dog"];
console.log(animals.lastIndexOf("cats"));
console.log(animals.lastIndexOf("rat"));
console.log(animals.lastIndexOf("rodents"));
console.log(animals.lastIndexOf("dog"));
